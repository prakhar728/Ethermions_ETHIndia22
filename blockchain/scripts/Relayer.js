const Address = '0x1Df02bFC7b2d955FBBaae8e5f98A4E6c9d02FD21';

async function main(signer, reciepent, tokenURI) {
	const nft = new ethers.Contract(Address, ABI, signer);
	try {
		const tx = await nft.safeMint(reciepent,tokenURI,{
			gasLimit: 1000000
		});
    await tx.wait();
    const currentTokenId = await nft.getCurrentTokenId()
		return currentTokenId;
	}
	catch (err) {
		return err
	}

}

exports.handler = async function (credentials) {
	const provider = new DefenderRelayProvider(credentials);
	const signer = new DefenderRelaySigner(credentials, provider, { gasLimit: 100000 });
	const {
		body,    // Object with JSON-parsed POST body
		headers, // Object with key-values from HTTP headers
		queryParameters, // Object with key-values from query parameters
	} = credentials.request;
	const Reciepent = body.reciever;
	const tokenURI = (body.tokenUri);
	console.log(`Using Relayer:${await signer.getAddress()}`);
	const run = await main(signer, Reciepent, tokenURI);
	return run;
}

